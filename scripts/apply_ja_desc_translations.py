def apply_desc_translations() -> dict[str, int]:
    changed: dict[str, int] = {}
    for filename, key_patches in DESC_TRANSLATIONS.items():
        if not key_patches:
            continue
        filepath = JA_DIR / filename
        if not filepath.exists():
            print(f"WARN: {filepath} does not exist, skipping")
            continue
        data = json.loads(filepath.read_text(encoding="utf-8"))
        count = 0
        for key, new_desc in key_patches.items():
            if key not in data:
                continue  # don't create missing keys for desc-only patches
            entry = data[key]
            if not isinstance(entry, dict):
                continue
            if entry.get("description") != new_desc:
                entry["description"] = new_desc
                count += 1
        filepath.write_text(
            json.dumps(data, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
        if count:
            changed[filename] = count
            print(f"  {filename}: {count} description(s) updated")
    return changed


if __name__ == "__main__":
    print("Applying Japanese description translations…")
    totals = apply_desc_translations()
    total_changes = sum(totals.values())
    print(f"\nDone: {len(totals)} file(s) modified, {total_changes} description(s) updated.")
